#crear un codigo de ejemplo que permita ingresar un numero entero y muestre su cuadrado y su cubo

#ingreso de datos
numero = int(input("Ingrese un numero entero: "))
#proceso
cuadrado = numero ** 2
cubo = numero ** 3
#salida
print("El cuadrado de", numero, "es", cuadrado)
print("El cubo de", numero, "es", cubo)

